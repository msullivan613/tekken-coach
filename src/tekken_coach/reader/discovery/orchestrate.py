def _frozen_heap_source(
    live: MemorySource, progress: Progress | None
) -> tuple[MemorySource, list[Region]]:  # pragma: no cover - live-only
    """Freeze every enumerated heap region over the live handle, returning (source, buffers).

    A ``LayeredMemorySource`` serves the frozen heap bytes for reads inside a region and falls back
    to the live handle for the static module (which does not move). This is what gives the scan a
    coherent round-start / post-action / post-reset snapshot from a handle that only reads *now*.
    """
    from tekken_coach.reader.discovery.basescan import LayeredMemorySource  # noqa: PLC0415
    from tekken_coach.reader.discovery.heapscan import _region_buffers  # noqa: PLC0415

    buffers = _region_buffers(live, live.regions(), progress=progress)
    return LayeredMemorySource(buffers, live), buffers


def run_update_offsets_derive(
    process_name: str,
    *,
    offsets_dir: str | Path = DEFAULT_OFFSETS_DIR,
    manifest_path: str | Path = DEFAULT_MANIFEST_PATH,
    version_override: str | None = None,
    act_prompt: Callable[[str], None] | None = None,
    progress: Progress | None = None,
) -> tuple[OffsetTable | None, DiagnosticReport]:  # pragma: no cover - attaches to a live game
    """C4h live re-discovery: derive the whole layout from behavior, no seeded within-struct offset.

    Steps: attach read-only -> detect version -> **freeze the heap at round start** -> prompt the
    user to act (:data:`DERIVE_ACT_PROMPT`), sampling a series of frozen heap snapshots across the
    action window -> prompt for a **round reset** (:data:`DERIVE_RESET_PROMPT`) and freeze the
    reallocated heap -> derive (locate by behavior, reverse-scan for a durable static path, derive
    every field) -> build + write the table. Windows-only, user-run; everything below the attach is
    offline-tested via :func:`discover_derive` against a planted enumerable image with a reallocated
    variant. Reads the heap through the read-only seam and writes a *file*, never the game (§2).
    """
    from tekken_coach.reader.version import detect_running_version  # noqa: PLC0415
    from tekken_coach.reader.win_source import WinMemorySource  # noqa: PLC0415

    prompt = act_prompt if act_prompt is not None else _default_act_prompt
    manifest = load_probe_manifest(manifest_path)
    seed, seed_version = _load_seed(offsets_dir)
    state_map = load_state_map(state_map_path(manifest, manifest_path))

    live: MemorySource = WinMemorySource(process_name)
    version = version_override or detect_running_version(process_name)
    module_base = live.module_base(manifest.module)

    # Round start: freeze the heap (the struct is stable within a round).
    _emit_progress(progress, "  freezing the heap at round start ...")
    before, before_buffers = _frozen_heap_source(live, progress)

    prompt(_derive_prompt(manifest))
    _countdown(manifest.action_lead_in_seconds, progress)

    # The action window: a series of frozen heap snapshots, so a transient move_id that idles back
    # is still caught (the C4g any-sample rule). Sampling freezes each instant coherently.
    during: list[MemorySource] = []
    for _ in _sampling_window(
        live,
        seconds=manifest.action_window_seconds,
        interval=manifest.action_sample_interval,
        progress=progress,
    ):
        snapshot, _buffers = _frozen_heap_source(live, None)
        during.append(snapshot)
    after = during[-1] if during else before

    prompt(DERIVE_RESET_PROMPT)
    _emit_progress(progress, "  freezing the reallocated heap ...")
    realloc, _realloc_buffers = _frozen_heap_source(live, progress)

    inputs = DeriveInputs(
        before=before,
        during=during,
        after=after,
        realloc=realloc,
        heap_buffers=before_buffers,
    )
    table, report = discover_derive(
        inputs,
        module=manifest.module,
        module_base=module_base,
        game_version=version,
        manifest=manifest,
        seed=seed,
        seed_version=seed_version,
        state_map=state_map,
    )
    if table is not None:
        persist(table, report, offsets_dir=offsets_dir)
    return table, report


