def run_update_offsets_holder(
    process_name: str,
    *,
    offsets_dir: str | Path = DEFAULT_OFFSETS_DIR,
    manifest_path: str | Path = DEFAULT_MANIFEST_PATH,
    version_override: str | None = None,
    act_prompt: Callable[[str], None] | None = None,
    progress: Progress | None = None,
    debug_dir: str | Path | None = None,
) -> tuple[OffsetTable | None, DiagnosticReport]:  # pragma: no cover - attaches to a live game
    """C4i live re-discovery: adopt the holder model (AoB code-sig -> holder -> per-player slots).

    Steps: attach read-only -> detect version -> parse the PE -> **find the holder by its AoB code
    signature** and validate it structurally at round start -> sweep the global-struct candidates ->
    freeze both player structs -> prompt the user to act (:data:`HOLDER_ACT_PROMPT`) -> sample a
    timed action window, confirming the acting player's ``move_id`` moved and the global frame
    counter advanced by the window's measured duration -> run the position scan on the frozen
    structs -> build + write the table + register the version. Windows-only, user-run; everything
    below the attach is offline-tested via :func:`discover_holder` against a planted holder world.

    The holder is found **once, before the prompt** (a code signature is a fast, unique identity, so
    there is no candidate sweep), and confirmed **after** it, because the behavioral oracle needs
    the acting player's ``move_id`` across the window and a live handle only reads *now*.
    """
    from tekken_coach.reader.discovery.basescan import (  # noqa: PLC0415
        LayeredMemorySource,
        confirm_global,
        frame_delta_band,
        freeze_struct,
        global_candidates,
        rebase_global_candidates,
    )
    from tekken_coach.reader.discovery.holderscan import find_holder_slot, resolve_holder
    from tekken_coach.reader.discovery.pe import parse_module_image  # noqa: PLC0415
    from tekken_coach.reader.version import detect_running_version  # noqa: PLC0415
    from tekken_coach.reader.win_source import WinMemorySource  # noqa: PLC0415

    prompt = act_prompt if act_prompt is not None else _default_act_prompt
    manifest = load_probe_manifest(manifest_path)
    seed, seed_version = _load_seed(offsets_dir)
    state_map = load_state_map(state_map_path(manifest, manifest_path))
    if manifest.holder_scan is None:
        raise OffsetTableError(
            "no holder_scan spec in the probe manifest; cannot run --holder-scan."
        )

    live: MemorySource = WinMemorySource(process_name)
    version = version_override or detect_running_version(process_name)
    module_base = live.module_base(manifest.module)
    image = parse_module_image(lambda rva, n: live.read(module_base + rva, n))
    if progress is not None:
        progress(
            f"  parsed PE: SizeOfImage {image.size_of_image // 1024} KiB, "
            f"{len(image.sections)} sections"
        )

    # Instant A: locate the holder by its AoB code signature and validate it structurally.
    slot_rva = find_holder_slot(
        live,
        module_base,
        image,
        pattern=manifest.holder_scan.aob_pattern,
        disp32_pos=manifest.holder_scan.disp32_pos,
        progress=progress,
    )
    holder = (
        resolve_holder(live, module_base, slot_rva, manifest.holder_scan, manifest)
        if slot_rva is not None
        else None
    )

    globals_ = []
    if manifest.global_scan is not None:
        globals_ = global_candidates(
            live,
            module=manifest.module,
            module_base=module_base,
            image=image,
            spec=manifest.global_scan,
            progress=progress,
        )

    # Freeze both player structs at round start so the position scan has a coherent *before*.
    # `freeze_struct` shrinks to the largest readable prefix: the manifest struct_span (0x40000) is
    # far larger than the real allocation, so an all-or-nothing full read raises and would drop the
    # whole freeze (the field we need sits in the first few KiB).
    frozen_before: list[Region] = []
    if holder is not None and manifest.base_scan is not None:
        span = manifest.base_scan.struct_span
        for base in holder.player_bases:
            region = freeze_struct(live, base, span)
            if region is not None:
                frozen_before.append(region)
        _emit_progress(
            progress,
            f"  froze {len(frozen_before)} struct(s) at round start "
            f"({', '.join(f'0x{len(r.data):x}B' for r in frozen_before) or 'none'})",
        )

    prompt(_holder_prompt(manifest))
    _countdown(manifest.action_lead_in_seconds, progress)

    opened = time.monotonic()
    if globals_ and manifest.global_scan is not None:
        globals_ = rebase_global_candidates(
            live,
            globals_,
            module=manifest.module,
            module_base=module_base,
            spec=manifest.global_scan,
        )

    # Freeze the player structs at EACH sample instant. `_sampling_window` yields the live handle,
    # but `confirm_holder` reads `during` only after the window closes — by then move_id has idled
    # back, so live references would all read the same post-window value and the behavioral oracle
    # would never see a change (the transient move_id is gone). A per-sample snapshot of the two
    # struct spans captures move_id AT that instant; the holder/slot pointers are stable within a
    # round, so we layer over the live handle for the chain resolution and freeze only the fields.
    # This mirrors `frozen_before`/`frozen_after` above (and the C4h derive path's freeze).
    span = manifest.base_scan.struct_span if manifest.base_scan is not None else 0
    during: list[MemorySource] = []
    debug_samples: list[dict[str, object]] = []
    for _sample in _sampling_window(
        live,
        seconds=manifest.action_window_seconds,
        interval=manifest.action_sample_interval,
        progress=progress,
    ):
        frozen_sample: list[Region] = []
        if holder is not None and span:
            for base in holder.player_bases:
                region = freeze_struct(live, base, span)
                if region is not None:
                    frozen_sample.append(region)
        during.append(LayeredMemorySource(frozen_sample, live) if frozen_sample else live)
        if debug_dir is not None and holder is not None:
            debug_samples.append(_holder_sample_debug(frozen_sample, holder, manifest))
    elapsed = time.monotonic() - opened

    global_located = None
    if globals_ and manifest.global_scan is not None:
        global_located = confirm_global(
            live,
            globals_,
            module=manifest.module,
            module_base=module_base,
            spec=manifest.global_scan,
            frame_delta=frame_delta_band(manifest.global_scan, elapsed),
            progress=progress,
        )

    # Freeze the after struct for the position scan, re-resolving the (possibly reallocated) bases.
    before_source: MemorySource = (
        LayeredMemorySource(frozen_before, live) if frozen_before else live
    )
    after_source: MemorySource = live
    if holder is not None and manifest.base_scan is not None:
        span = manifest.base_scan.struct_span
        from tekken_coach.reader.discovery.holderscan import _resolve_players  # noqa: PLC0415

        after = _resolve_players(live, module_base, holder.slot_rva, manifest.holder_scan)
        if after is not None:
            frozen_after: list[Region] = []
            for base in after[1]:
                region = freeze_struct(live, base, span)
                if region is not None:
                    frozen_after.append(region)
            if frozen_after:
                after_source = LayeredMemorySource(frozen_after, live)

    table, report = discover_holder(
        before_source,
        module=manifest.module,
        module_base=module_base,
        game_version=version,
        manifest=manifest,
        seed=seed,
        seed_version=seed_version,
        source_after=after_source,
        during=during,
        holder=holder,
        slot_rva=slot_rva,
        global_located=global_located,
        sweep_global=False,
        state_map=state_map,
    )
    if debug_dir is not None:
        path = _write_holder_debug(
            debug_dir,
            holder=holder,
            slot_rva=slot_rva,
            manifest=manifest,
            samples=debug_samples,
            elapsed=elapsed,
        )
        _emit_progress(progress, f"  wrote holder-scan debug capture to {path}")
    if table is not None:
        persist(table, report, offsets_dir=offsets_dir)
    return table, report


def _holder_sample_debug(
    frozen: Sequence[Region], holder: HolderMatch, manifest: ProbeManifest
) -> dict[str, object]:  # pragma: no cover - live-only debug capture
    """Decode P1/P2 move_id + damage_taken from one frozen window sample for the debug dump.

    Reads at the *frozen* bases (what the freeze actually captured); a player that failed to freeze
    shows ``frozen: False`` — itself the signal that the full-span read overran the allocation.
    """
    spec = manifest.holder_scan
    assert spec is not None
    by_base = {r.base: r for r in frozen}
    players: list[dict[str, object]] = []
    for base in holder.player_bases:
        region = by_base.get(base)
        if region is None:
            players.append({"base": hex(base), "frozen": False})
            continue
        players.append(
            {
                "base": hex(base),
                "frozen": True,
                "span": hex(len(region.data)),
                "move_id": region.read_scalar(base + spec.move_id_offset, manifest.move_id_kind),
                "damage_taken": region.read_scalar(base + spec.damage_taken_offset, "i32"),
            }
        )
    return {"players": players}


def _write_holder_debug(
    debug_dir: str | Path,
    *,
    holder: HolderMatch | None,
    slot_rva: int | None,
    manifest: ProbeManifest,
    samples: list[dict[str, object]],
    elapsed: float,
) -> Path:  # pragma: no cover - live-only debug capture
    """Dump the round-start landing + per-sample move_id/damage series to a JSON for offline review.

    This live path is untestable in CI, so a structured capture of what each window sample actually
    read (the frozen span achieved, and P1/P2 move_id at every instant) is how a failed behavioral
    oracle gets diagnosed without another live round-trip.
    """
    spec = manifest.holder_scan
    round_start: dict[str, object] | None = None
    if holder is not None:
        round_start = {
            "holder_base": hex(holder.holder_base),
            "player_bases": [hex(b) for b in holder.player_bases],
            "char_ids": list(holder.char_ids),
            "move_ids": list(holder.move_ids),
        }
    out: dict[str, object] = {
        "slot_rva": hex(slot_rva) if slot_rva is not None else None,
        "moving_player": manifest.moving_player,
        "move_id_offset": hex(spec.move_id_offset) if spec is not None else None,
        "damage_taken_offset": hex(spec.damage_taken_offset) if spec is not None else None,
        "struct_span": (
            hex(manifest.base_scan.struct_span) if manifest.base_scan is not None else None
        ),
        "action_window_seconds": manifest.action_window_seconds,
        "action_sample_interval": manifest.action_sample_interval,
        "window_elapsed_seconds": round(elapsed, 3),
        "round_start": round_start,
        "samples": samples,
    }
    directory = Path(debug_dir)
    directory.mkdir(parents=True, exist_ok=True)
    path = directory / "holder-scan-debug.json"
    path.write_text(json.dumps(out, indent=2), encoding="utf-8")
    return path


def _emit_progress(progress: Progress | None, message: str) -> None:  # pragma: no cover - live-only
    if progress is not None:
        progress(message)


def _read_snapshots(
    source: MemorySource, manifest: ProbeManifest
) -> tuple[Region, Region]:  # pragma: no cover - live-only
    """Read the player + global scan windows into a ``(player_region, global_region)`` pair."""
    player = snapshot_region(
        source,
        manifest.module,
        manifest.player_window.base_offset,
        manifest.player_window.size,
        absolute=manifest.player_window.absolute,
    )
    global_ = snapshot_region(
        source,
        manifest.module,
        manifest.global_window.base_offset,
        manifest.global_window.size,
        absolute=manifest.global_window.absolute,
    )
    return player, global_


def _countdown(seconds: float, progress: Progress | None) -> None:  # pragma: no cover - live-only
    """Give the user time to alt-tab back to the game before the window opens.

    Without this the window would start while they are still looking at the terminal, and the first
    second or two of samples would show an idle character — harmless for the ``any``-sample oracle,
    wasteful for a window the user is asked to keep moving through.
    """
    remaining = seconds
    while remaining > 0:
        if progress is not None:
            progress(f"  capturing in {math.ceil(remaining)}s ...")
        step = min(1.0, remaining)
        time.sleep(step)
        remaining -= step


def _sampling_window(
    source: MemorySource, *, seconds: float, interval: float, progress: Progress | None
) -> Iterator[MemorySource]:  # pragma: no cover - live-only (offline: a list of planted snapshots)
    """Yield ``source`` on a fixed cadence for ``seconds``, as one "snapshot" per sample.

    The live half of the seam :func:`~.basescan.confirm_players` reads through. Live, every sample
    is the same process handle read at a different instant; offline it is a different planted image
    each time. The oracle cannot tell, which is exactly why the transient-``move_id`` behavior it
    now depends on is testable without a running game. Always yields at least once.
    """
    opened = time.monotonic()
    deadline = opened + seconds
    taken = 0
    while True:
        yield source
        taken += 1
        remaining = deadline - time.monotonic()
        if remaining <= 0:
            break
        time.sleep(min(interval, remaining))
    if progress is not None:
        elapsed = time.monotonic() - opened
        progress(f"  action window closed: {taken} samples over {elapsed:.1f}s")


def _default_act_prompt(message: str) -> None:  # pragma: no cover - interactive, live-only
    input(message)
